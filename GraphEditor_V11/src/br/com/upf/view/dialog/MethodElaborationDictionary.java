    public MethodElaborationDictionary(GraphEditor editor, ClassElement element) {
        super(editor, true);
        this.graphEditor = editor;
        this.classElement = element;
        this.classModel = classElement.getClassModel();
        initComponents();
        if (classModel.isInitializedDictionaryOfStates()) {
            if (classModel.isMethodElaborationCustom()) {
                jComboBoxMethod.setSelectedIndex(2);
            }
            if (!classModel.isMethodElaborationCustom()) {
                jComboBoxMethod.setSelectedIndex(1);
            }
        }

        updateTitle();
    }

