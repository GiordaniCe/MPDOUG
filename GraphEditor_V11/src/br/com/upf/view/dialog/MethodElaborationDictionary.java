    /**
     * Ação do JComboBox - Seleção de Método de elaboração
     *
     * @param evt
     */
    private void jComboBoxMethodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxMethodActionPerformed
        Integer selectIndexMethod = jComboBoxMethod.getSelectedIndex();
        CardLayout buildPanel = (CardLayout) jPanelMethodInformation.getLayout();

        switch (selectIndexMethod) {
            case 1:
                buildPanel.show(jPanelMethodInformation, "card1");
                break;
            case 2:
                buildPanel.show(jPanelMethodInformation, "card2");
                break;

            default:
                buildPanel.show(jPanelMethodInformation, "card0");
        }

    }//GEN-LAST:event_jComboBoxMethodActionPerformed

