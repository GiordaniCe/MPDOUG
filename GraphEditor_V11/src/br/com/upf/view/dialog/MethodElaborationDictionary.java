    /**
     * Ação do Botão - Salvar
     *
     * @param evt
     */
    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        Integer selectIndexMethod = jComboBoxMethod.getSelectedIndex();
        if (selectIndexMethod != 0) {
            switch (selectIndexMethod) {
                case 1:
                    classModel.setMethodElaborationCustom(false);
                    break;
                case 2:
                    classModel.setMethodElaborationCustom(true);
                    break;
            }
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione o Método de Elaboração!");
            jComboBoxMethod.requestFocus();
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

